using UnityEngine;
using UnityEngine.XR;

public class Player_BasicAttackState : PlayerState
{
    private float lastTimeAttack;
    private float attackVelocityTimer;

    private bool comboAttackQueued;
    private int attackDir;
    private int comboIndex = 1;
    private int comboLimit = 3;

    private const int FirstComboIndex = 1;
    public Player_BasicAttackState(Player player, StateMachine stateMachine, string animBoolName) : base(player, stateMachine, animBoolName)
    {
        if (comboLimit != player.attackVelocity.Length)
        {
            Debug.LogWarning("combo limit changed according to attack velocity array");
            comboLimit = player.attackVelocity.Length;
        }
    }

    public override void Enter()
    {
        base.Enter();
        comboAttackQueued = false;
        ResetComboIndexIfNeeded();

        attackDir = player.moveInput.x != 0 ? ((int)player.moveInput.x) : player.facingDiraction;

        anim.SetInteger("basicAttackIndex", comboIndex);
        ApplyAttackVelocity();

    }
    public override void Update()
    {
        base.Update();
        if (input.Player.Attack.WasPressedThisFrame())
            QueueNextAttack();

        HandleAttacklVelocity();

        if (triggerCalled) HandleStateExit();


    }


    public override void Exit()
    {
        base.Exit();
        comboIndex++;
        lastTimeAttack = Time.time;
    }

    private void HandleStateExit()
    {
        if (comboAttackQueued)
        {
            anim.SetBool(animBoolName, false);
            player.EnterAttackStateWithDelay();
        }
        else
        {
            stateMachine.ChangeState(player.idleState);
        }
    }
    private void QueueNextAttack()
    {
        if (comboIndex < comboLimit) comboAttackQueued = true;
    }


    private void HandleAttacklVelocity()
    {
        attackVelocityTimer -= Time.deltaTime;
        if (attackVelocityTimer < 0)
        {
            player.SetVelocity(0, rb.linearVelocity.y);
        }
    }

    private void ApplyAttackVelocity()
    {
        Vector2 attackVelocity = player.attackVelocity[comboIndex - 1];
        attackVelocityTimer = player.attackVelocityDuration;

        player.SetVelocity(attackVelocity.x * attackDir, attackVelocity.y);
    }

    private void ResetComboIndexIfNeeded()
    {
        if (comboIndex > comboLimit || Time.time > lastTimeAttack + player.comboResetTime)
        {
            comboIndex = FirstComboIndex;
        }
    }
}

